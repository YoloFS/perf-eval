/// Stylized backend names for publication.
pub fn backend_display_name(name: &str) -> &'static str {
    match name {
        "native" => "Base",
        "yolo-no-perm" => "\\fs",
        "yolo-realistic" => "\\fs",
        "yolo" => "\\fs",
        "overlayfs" => "OverlayFS",
        "branchfs" => "BranchFS",
        _ => "Unknown",
    }
}

/// Escape special LaTeX characters.
pub fn latex_escape(s: &str) -> String {
    // Preserve known LaTeX commands.
    if s.starts_with('\\') && !s.contains(' ') {
        return s.to_string();
    }
    s.replace('\\', "\\textbackslash{}")
        .replace('&', "\\&")
        .replace('%', "\\%")
        .replace('$', "\\$")
        .replace('#', "\\#")
        .replace('_', "\\_")
        .replace('{', "\\{")
        .replace('}', "\\}")
        .replace('~', "\\textasciitilde{}")
        .replace('^', "\\textasciicircum{}")
}

/// Write the shared plot_style.py module to `paper_dir` (if not already up to date).
/// Returns the Python code to import it at the top of a figure script.
pub fn ensure_plot_style(paper_dir: &Path) -> Result<()> {
    let path = paper_dir.join("plot_style.py");
    std::fs::write(&path, PLOT_STYLE_PY).with_context(|| format!("writing {}", path.display()))?;
    Ok(())
}

